public class Forquilla {
    private int index;
    private boolean enUs = false;

    public Forquilla(int index) {
        this.index = index;
    }
    

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }


    public boolean getEnUs() {
        return enUs;
    }

    public void setEnUs(boolean enUs) {
        this.enUs = enUs;
    }
}
